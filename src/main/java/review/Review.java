package review;

public class Review {
	
	long id;
	String title;
	String imageurl;
	String reviewCategory;
	String content;
	public long getId() {
		return id;
	}
	public String getTitle() {
		return title;
	}

	public String getImageurl() {
		return imageurl;
	}

	public String getReviewCategory() {
		return reviewCategory;
	}

	public String getContent() {
		return content;
	}

	
	
}
